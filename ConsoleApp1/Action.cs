using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ConsoleApp1
{
    class Actions
    {
        public static int Increaser(int x, int y)
        {
            return x + y;

        }

        public static int Decreaser(int x, int y)
        {
            return x - y;
        }
        public static int Multiply(int x, int y)
        {
            return x * y;
        }

        public static int Split(int x, int y)
        {
            return x / y;
        }
    }
                
}
